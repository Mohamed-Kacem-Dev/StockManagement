    public static void  updateArticleTable(){
try {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs= st.executeQuery("select Article,marque,prixGros,prixDetail from article;");
            jArticleTable.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) { 
  System.out.println(e);
        }
}
