            String marque=(jArticleTable.getModel().getValueAt(row, 1).toString());
           String prixGros=(jArticleTable.getModel().getValueAt(row, 2).toString());
           String prixDetail=(jArticleTable.getModel().getValueAt(row, 3).toString());
            int id=-1;
            Statement st3=con.createStatement();
            ResultSet rs1= st3.executeQuery("select ArticleID from Article where Article='"+article+"' and marque='"+marque+"';");
           while(rs1.next()){ id=rs1.getInt("ArticleID");}
